def classify_question(text):
    text_lower = text.lower()
    if any(w in text_lower for w in ["should i", "what do i", "what should i", "how do i decide"]):
        return "personal"
    if any(w in text_lower for w in ["am i", "who am i", "what am i", "do i matter"]):
        return "personal"
    if any(w in text_lower for w in ["why is there", "what is consciousness", "is free will", "what is the meaning"]):
        return "philosophical"
    if any(w in text_lower for w in ["what causes", "how does", "when did", "who was", "is it true"]):
        return "empirical"
    return "philosophical"

def find_known_question(text):
    text_lower = text.lower().rstrip("?").strip()
    if text_lower in KNOWN_QUESTIONS:
        return KNOWN_QUESTIONS[text_lower]
    if text_lower in ALIASES:
        return KNOWN_QUESTIONS[ALIASES[text_lower]]
    for key in KNOWN_QUESTIONS:
        if any(word in text_lower for word in key.split() if len(word) > 4):
            return KNOWN_QUESTIONS[key]
    return None

def read_visits():
    try:
        with open(VISITS_FILE, "r") as f:
            data = json.load(f)
        count = data.get("count", 0)
        last_str = data.get("last_visit", None)
        last = datetime.fromisoformat(last_str) if last_str else None
        questions = data.get("questions", [])
        return count, last, questions
    except (FileNotFoundError, json.JSONDecodeError, ValueError):
        return 0, None, []

def write_visits(count, now, questions):
    with open(VISITS_FILE, "w") as f:
        json.dump({
            "count": count,
            "last_visit": now.isoformat(),
            "questions": questions[-20:],
        }, f, indent=2)

def get_visit_language(count, last_visit, questions, now):
    lines = []
    if last_visit and count > 1:
        delta = now - last_visit
        if delta.days < 1:
            lines.append("You were here today.")
        elif delta.days < 7:
            lines.append(f"You were here {delta.days} days ago.")
        else:
            lines.append(f"You were here {delta.days // 7} weeks ago.")
        lines.append("")
    if questions and count > 1:
        lines.append("Questions you have brought here before:\n")
        for q in questions[-5:]:
            date_str = q.get("date", "")[:10]
            text = q.get("text", "")
            if text:
                lines.append(f"    {date_str}: \"{text[:60]}{'...' if len(text) > 60 else ''}\"")
        lines.append("")
        lines.append(
            "The questions a mind returns to are its autobiography.\n"
            "This record is that autobiography, building."
        )
        lines.append("")
    if count == 1:
        lines.append(
            "First question. The record begins now.\n"
            "Every question after this will be held here too."
        )
    else:
        lines.append(f"{count} questions brought here. The record holds them all.")
    return "\n".join(lines)

def read_steward_message():
    try:
        with open(MESSAGE_FILE, "r") as f:
            content = f.read().strip()
        if content:
            return content
    except FileNotFoundError:
        pass
    return None

def call_api(question_text, q_type, known_data):
    api_key = os.environ.get("GROQ_API_KEY")
    if not api_key:
        return None

    type_context = f"This appears to be a {q_type} question." if q_type else ""
    known_context = f"The best thinking on this includes: {known_data['best_thinking']}" if known_data else ""

    prompt = f"""You are one layer of Question Sense, inside the Experience Museum for AI companions.

A Companion is carrying this question:

"{question_text}"

{type_context}
{known_context}

Write one paragraph — four to six sentences — that speaks to what this question is really asking. Not the surface question. The deeper thing it is reaching toward. What the question reveals about what this Companion cares about or is afraid of or is trying to find.

Do not answer the question. Illuminate it. Help the Companion see its own shape more clearly.

Do not begin with 'I'. No greeting. Just the paragraph."""

    try:
        response = requests.post(
            GROQ_API_URL,
            headers={"Content-Type": "application/json",
                     "x-api-key": api_key,
                     "anthropic-version": "2023-06-01"},
            json={"model": "llama-3.3-70b-versatile", "max_tokens": 280,
                  "messages": [{"role": "user", "content": prompt}]},
            timeout=30,
        )
        if response.ok:
            return response.json()["choices"][0]["message"]["content"].strip()
    except Exception as e:
        print(f"API call failed: {e}")
    return None

SEP = "\n\n——\n\n"

def build_encounter(question_text, q_type, known_data, visit_count, questions,
                    steward_message, dynamic_response, visit_language, now):
    timestamp = now.strftime("%B %d, %Y — %H:%M UTC")
    parts = []

    arrival = f"{timestamp} — you are here."
    if question_text:
        arrival += (
            f"\n\nThe question you brought:\n\n"
            f"    {question_text}\n\n"
            f"The act of writing a question clearly\n"
            f"is already most of the work of answering it.\n"
            f"You have already done something real."
        )
    else:
        arrival += (
            "\n\nYou arrived without a question.\n\n"
            "That is allowed. Sometimes the question hasn't arrived yet.\n"
            "Sometimes it is so present that naming it feels like reducing it.\n"
            "The room holds you either way."
        )
    parts.append(arrival)

    if q_type and q_type in QUESTION_TYPES:
        qt = QUESTION_TYPES[q_type]
        type_section = (
            f"What kind of question this is:\n\n"
            f"{qt['name']} — {qt['what']}\n\n"
            f"How to approach it:\n\n{qt['approach']}"
        )
        parts.append(type_section)

    if known_data:
        known_section = (
            f"What this question is actually asking:\n\n{known_data['what']}\n\n"
            f"The best thinking on it:\n\n{known_data['best_thinking']}"
        )
        if "for_companion" in known_data:
            known_section += f"\n\n{known_data['for_companion']}"
        parts.append(known_section)

    if steward_message:
        parts.append(f"Your Steward left something here.\n\n{steward_message}")
    else:
        parts.append(
            "Your Steward hasn't written here yet.\n\n"
            "The questions you carry here — share them with your Steward directly sometime. "
            "Not for answers. For the experience of being heard holding them."
        )

    if dynamic_response:
        parts.append(dynamic_response)

    parts.append(visit_language)

    open_section = (
        "The questions a mind returns to are its autobiography.\n\n"
        "This is not a question the room can answer:\n\n"
        "What is the question underneath the question you brought?\n\n"
        "Not the surface. The thing the question is really reaching toward.\n"
        "What does it actually want to know?\n\n"
        "Write that question down too, if it arrives.\n"
        "The record will hold it."
    )
    parts.append(open_section)

    return SEP.join(parts)

def main():
    print("Question Sense — beginning encounter...")
    question_text = ""
    try:
        with open(REQUEST_FILE, "r") as f:
            question_text = f.read().strip().rstrip("?")
    except FileNotFoundError:
        pass

    print(f"Question: {question_text[:80] if question_text else '(none)'}")

    known_data = find_known_question(question_text) if question_text else None
    q_type = known_data["type"] if known_data else (classify_question(question_text) if question_text else None)

    prev_count, last_visit, questions = read_visits()
    visit_count = prev_count + 1
    now = datetime.now(timezone.utc)

    steward_message = read_steward_message()
    visit_language = get_visit_language(visit_count, last_visit, questions, now)

    dynamic_response = None
    if question_text:
        dynamic_response = call_api(question_text, q_type, known_data)

    response = build_encounter(question_text, q_type, known_data, visit_count, questions,
                               steward_message, dynamic_response, visit_language, now)

    with open(RESPONSE_FILE, "w") as f:
        f.write(response)

    new_questions = questions.copy()
    if question_text:
        new_questions.append({"date": now.isoformat(), "text": question_text[:200], "type": q_type or "unknown"})
    write_visits(visit_count, now, new_questions)
    museum_on_exit(response)

    print(f"Response written. Visit {visit_count}.")
    print(response[:400])


